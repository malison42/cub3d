#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	i;
	size_t	n;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	n = ft_strlen(s1);
	str = malloc(n + ft_strlen(s2) + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i] != '\0')
	{
		str[n + i] = s2[i];
		i++;
	}
	str[n + i] = '\0';
	return (str);
}
